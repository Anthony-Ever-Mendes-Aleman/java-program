package MathOperations;

public class MathOperations {
    public double add(double a, double b)
    {
        return a + b;
    }
    public int add(int a, int b)
    {
        return a + b;
    }
    public int multiply(int a, int b)
    {
        return a * b;
    }
    public double multiply(double a, double b)
    {
        return a * b;
    }
}
